package org.akonakanchi.java.dataTypes;

public class PrimitiveDataTypes {

//    These are the only datatypes that are not Objects or classes in java and called primitive types

    //stores whole numbers occupies 4 bytes (32 bits)
    int i =10101010;
    // float and double stores decimal numbers but float round's off
    double d =10.1234;
    float f = 10.2345f;
    // stores whole numbers occupies 8 bytes(64 bit) longer than int
    long l = 102000l;
    //2 bites whole number
    short s = 123;
    // byte is 8 bits - whole number
    byte b = 12;
    // one letter in a string
    char c = 'c';
    // can store true or false
    boolean bl = true;


    //Wrapper Class
    Integer myInt = i;

}
